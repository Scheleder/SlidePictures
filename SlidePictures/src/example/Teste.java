    private void jLabel1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel1MouseClicked

        tempo.stop();

        String textoDoJPane = JOptionPane.showInputDialog(null, "Intervalo em segundos:", (intervalo / 1000));
        if (textoDoJPane != null) {
            boolean validaNumero = false;
            for (int i = 0; i < textoDoJPane.length(); i++) {
                if (Character.isDigit(textoDoJPane.charAt(i))) {
                    validaNumero = true;
                } else {
                    validaNumero = false;
                    JOptionPane.showMessageDialog(null, "Digite apenas números!");
                    textoDoJPane = JOptionPane.showInputDialog(null, "Intervalo em segundos:", (intervalo / 1000));
                }
            }
            intervalo = 1000 * Integer.parseInt(textoDoJPane);
            if (intervalo < 2000) {
                intervalo = 2000;
            }
        }
        System.out.println("intervalo " + intervalo + " milisegundos");
        tempo();
    }//GEN-LAST:event_jLabel1MouseClicked

