    private void slide() {

        item++;
        if (item > imagens.size() - 1) {
            item = 0;
            leitura();
        }

        try {
            jLabel1.setIcon(new ImageIcon(Toolkit.getDefaultToolkit().getImage((String) imagens.get(item)).getScaledInstance(width, height, Image.SCALE_SMOOTH)));

        } catch (IndexOutOfBoundsException e) {
            inicial();
        }
        //jLabel1.repaint();
        //pack();
        System.out.println(item);

    }

    private void leitura() {
        imagens.clear();
        File arquivo = new File("c:\\SlidePictures\\");
        File[] file = arquivo.listFiles();

        if (file != null) {
            int length = file.length;

            for (int i = 0; i < length; ++i) {
                File f = file[i];

                if (f.isFile()) {
                    System.out.println(f.getName());
                    if (!f.getName().equals("Thumbs.db")) {
                        imagens.add("c:/SlidePictures/" + f.getName());
                    }
                }
            }

        } else {
            inicial();
        }

    }

    private void resize() {
        Toolkit kit = Toolkit.getDefaultToolkit();
        Dimension tamTela = kit.getScreenSize();

        //Pega largura e altura da tela 
        width = tamTela.width;
        height = tamTela.height;

        /* larg x 0.7; para ocupar 70% da tela por exemplo  */
 /* alt x 0.7;*/
        //Manda o JFrame utilizar suas dimensões  
        setSize(width, height);
        getContentPane().add(jLabel1);
        //jLabel1.setLocation(0, 0);
        //jLabel no tamanho da tela
        jLabel1.setSize(tamTela);

    }

    private void tempo() {
        tempo = new Timer(intervalo, (ActionEvent e) -> {
            slide();
            mouseMove();
        } //vai esperar intervalo e executar essa ação
        );
        tempo.start();
    }

    private void inicial() {
        URL url = this.getClass().getResource("/imagens/slide.png");
        Icon img = new ImageIcon(Toolkit.getDefaultToolkit().getImage(url));
        jLabel1.setIcon(img);
    }

    private void mouseMove() {
        Robot robot;
        try {
            robot = new Robot();
            int X = MouseInfo.getPointerInfo().getLocation().x;
            int Y = MouseInfo.getPointerInfo().getLocation().y;
            if (X > width-2) {
                X = 1;
            }
            robot.mouseMove(X + 1, Y);
        } catch (AWTException ex) {
            Logger.getLogger(Teste.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
